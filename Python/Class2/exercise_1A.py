nro_one = int(input("Numero uno: "))
nro_two = int(input("Numero dos: "))

suma = nro_one + nro_two
resta = nro_one - nro_two
mult = nro_one * nro_two
div = nro_one / nro_two

print(str(nro_one) + " + " + str(nro_two) + " = " + str(suma))
print(str(nro_one) + " - " + str(nro_two) + " = " + str(resta))
print(str(nro_one) + " * " + str(nro_two) + " = " + str(mult))
print(str(nro_one) + " / " + str(nro_two) + " = " + str(div))
